package com.braintrust.snipe.status;

public class Status {
     private String status;
     private String process;

    public String getStatus()
    {
        return status;
    }

    public void setStatus(String s)
    {
        this.status = s;
    }

    public String getProcess()
    {
        return process;
    }

    public void setProcess(String p)
    {
        this.status = p;
    }

}
